package org.tnsindia.ArrayDemo;
//demo on to know the class of array
public class ArrayExample {

	public static void main(String[] args) {
		String[] arr= {"January","February","March","April"};
		/*getclass() returns runtime cls of theobject*/
		/*getName() returns the canonical name of the underlying class asdefined by */
		System.out.println(arr.getClass().getName());

	}

}
